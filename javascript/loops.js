// for loop
for (let i = 0; i < 5; i++) {
    console.log("Iteration:", i); // Outputs numbers 0 to 4
}

// while loop
let count = 0;
while (count < 3) {
    console.log("Count:", count); // Outputs 0, 1, 2
    count++;
}

// do...while loop
let num = 0;
do {
    console.log("Number:", num); // Outputs 0, then increments
    num++;
} while (num < 2);
