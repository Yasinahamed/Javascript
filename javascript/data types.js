// String
let name = "John Doe"; 
console.log(typeof name); // Output: string

// Number
let age = 30; 
console.log(typeof age); // Output: number

// Boolean
let isStudent = false;
console.log(typeof isStudent); // Outputs: boolean

// Undefined
let x; 
console.log(typeof x); // Outputs: undefined

// Null (type is object due to a historical bug)
let y = null; 
console.log(typeof y); // Outputs: object

// Object
let person = { firstName: "John", lastName: "Doe" };
console.log(typeof person); // Outputs: object

// Array (also considered an object)
let colors = ["red", "green", "blue"];
console.log(typeof colors); // Outputs: object
