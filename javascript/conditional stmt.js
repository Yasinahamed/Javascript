let age = 18;

// if...else statement
if (age < 18) {
    console.log("You are a minor.");
} else if (age === 18) {
    console.log("Congratulations, you just became an adult!");
} else {
    console.log("You are an adult.");
}

// switch statement
let day = 3;
switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    default:
        console.log("Invalid day");
}
