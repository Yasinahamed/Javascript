// Arithmetic operators
let a = 10;
let b = 5;
console.log(a + b); // Outputs: 15 (addition)
console.log(a - b); // Outputs: 5 (subtraction)
console.log(a * b); // Outputs: 50 (multiplication)
console.log(a / b); // Outputs: 2 (division)

// Comparison operators
console.log(a > b); // Outputs: true
console.log(a === b); // Outputs: false

// Logical operators
let c = true;
let d = false;
console.log(c && d); // Outputs: false (logical AND)
console.log(c || d); // Outputs: true (logical OR)
console.log(!c); // Outputs: false (logical NOT)
